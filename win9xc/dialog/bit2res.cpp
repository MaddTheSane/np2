int	bit2res_getsize(const BIT2RES *p) {

	int		ret;
	int		align;

	ret = sizeof(BITMAPINFOHEADER) + 4 * 16;
	align = ((p->x) + 7) / 8;
	ret += align * 4 * (p->y);
	return(ret);
}

void bit2res_sethead(BITMAPINFO *bi, const BIT2RES *p) {

	UINT	i;

	ZeroMemory(&bi->bmiHeader, sizeof(BITMAPINFOHEADER));
	bi->bmiHeader.biSize = sizeof(BITMAPINFOHEADER);
	bi->bmiHeader.biWidth = p->x;
	bi->bmiHeader.biHeight = p->y;
	bi->bmiHeader.biPlanes = 1;
	bi->bmiHeader.biBitCount = 4;
	bi->bmiHeader.biCompression = BI_RGB;
	bi->bmiHeader.biSizeImage = ((((p->x) + 7) / 2) & ~3) * (p->y);
	bi->bmiHeader.biXPelsPerMeter = 0;
	bi->bmiHeader.biYPelsPerMeter = 0;
	bi->bmiHeader.biClrUsed = 16;
	bi->bmiHeader.biClrImportant = p->pals;

	for (i=0; i<p->pals; i++) {
		bi->bmiColors[i].rgbRed = p->pal[i].p.r;
		bi->bmiColors[i].rgbGreen = p->pal[i].p.g;
		bi->bmiColors[i].rgbBlue = p->pal[i].p.b;
		bi->bmiColors[i].rgbReserved = PC_RESERVED;
	}
}

void bit2res_setdata(BYTE *dat, const BIT2RES *p) {

	int		align;
	int		x, y;
const BYTE	*pic;
	BYTE	bit;
	BYTE	last;
	BYTE	d, l;
	int		length;

	align = (((p->x) + 7) / 2) & ~3;

	x = 0;
	y = 0;
	pic = p->data;
	bit = 0;
	last = 0xff;

	while(1) {
		d = ((*pic) >> bit) & 3;
		bit += 2;
		if (bit >= 8) {
			bit &= 7;
			pic++;
		}
		if (d != last) {
			length = 1;
		}
		else {
			length = 0;
			while(1) {
				length <<= 3;
				l = ((*(WORD *)pic) >> bit) & 15;
				bit += 4;
				if (bit >= 8) {
					bit &= 7;
					pic++;
				}
				if (l & 8) {
					break;
				}
				length += l;
			}
			length += l - 7;
		}
		while(length--) {
			if (x & 1) {
				dat[x/2] |= d;
			}
			else {
				dat[x/2] = d << 4;
			}
			x++;
			if (x >= p->x) {
				x = 0;
				dat += align;
				y++;
				if (y >= p->y) {
					return;
				}
			}
		}
		last = d;
	}
}

